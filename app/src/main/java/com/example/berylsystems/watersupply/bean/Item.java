public class Item {
    String name;
    String waterRate;
    String bottleRate;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWaterRate() {
        return waterRate;
    }

    public void setWaterRate(String waterRate) {
        this.waterRate = waterRate;
    }

    public String getBottleRate() {
        return bottleRate;
    }

    public void setBottleRate(String bottleRate) {
        this.bottleRate = bottleRate;
    }
}
